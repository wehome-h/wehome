package com.wehome.p11;

public class JavaBean {

    // 属性一般定义为 private
    private String name;
    private int age;

    public JavaBean() {
    }

    public int ageAge() {
        return age;
    }

    public void setAge(int a) {
        age = a;
    }

    public String getName() {
        return name;
    }

    public void setName(String n) {
        name = n;
    }

}